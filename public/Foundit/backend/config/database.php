<?php

$host = "127.0.0.1";
$user = "root";
$pass = "";
$db   = "foundit_db";
$port = 3307;

/*
|--------------------------------------------------------------------------
| DATABASE CONNECTION
|--------------------------------------------------------------------------
*/

$conn = mysqli_connect($host, $user, $pass, $db, $port);

/*
|--------------------------------------------------------------------------
| CHECK CONNECTION
|--------------------------------------------------------------------------
*/

if (!$conn) {
    die("Database Connection Failed: " . mysqli_connect_error());
}

/*
|--------------------------------------------------------------------------
| SET UTF-8
|--------------------------------------------------------------------------
*/

mysqli_set_charset($conn, "utf8mb4");

?>